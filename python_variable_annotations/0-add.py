#!/usr/bin/env python3
"""
Function that adds floats and returns the result
"""


def add(a: float, b: float) -> float:
    """
    Args:
        a (float): first integer
        b (float): second integer

    Returns:
        float: the sum of a and b

    Examples:
        >>> add(1.2, 1.2)
        2.4
        >>> add(3.5, 3.6)
        7.1
        >>> add(1.2, 3.5)
        4.7
    """
    return a + b
