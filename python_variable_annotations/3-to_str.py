#!/usr/bin/env python3
"""
Function that takes a float and returns the string representation
"""


def to_str(n: float) -> str:
    """
    Args:
        n (float): the number to be changed to a string

    Returns:
        str: string representation of n

    Examples:
        >>> to_str(7.6)
        7.6
        >>> to_str(3.14)
        3.14
        >>> to_str(6.3)
        6.3
    """
    return str(n)
